public class Pet {
    private String name;
    private int age;
    private PetDetail detail;

    public PetDetail getDetail() {
        return detail;
    }

    public void setDetail(PetDetail detail) {
        this.detail = detail;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

}
